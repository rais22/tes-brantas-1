package com.example.jdbc.demo.model;

public class JabatanResult {
    private int id_jabatan;
    private String nm_jabatan;
    // getter dan setter

    public int getIdJabatan() {
        return id_jabatan;
    }

    public void setIdJabatan(int id_jabatan) {
        this.id_jabatan = id_jabatan;
    }

    public String getNamaJabatan() {
        return nm_jabatan;
    }

    public void setNamaJabatan(String nm_jabatan) {
        this.nm_jabatan = nm_jabatan;
    }
    
}